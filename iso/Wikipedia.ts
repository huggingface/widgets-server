export const NOT_ISO = (include_iso_2 = false) => {
	const isos = include_iso_2
		? union(
			new Set(Object.keys(MAPPING_ISO_639_1)),
			new Set(Object.keys(MAPPING_ISO_2_FLAT()))
		)
		: new Set(Object.keys(MAPPING_ISO_639_1))
	;
	return difference(
		new Set(WIKIPEDIAS),
		isos
	);
}
